    private void kode()
    {
       try {
            Connection conn=(Connection)Koneksi.configDB();
            Statement stt=conn.createStatement();
            String sql="select * from bantuan_rt03 order by Kode desc";
            ResultSet rs = stt.executeQuery(sql);
            if (rs.next()) {
                String kode = rs.getString("Kode").substring(3);
                String BAN = "" + (Integer.parseInt(kode) + 1);
                String Nol = "";

                if(BAN.length()==1)
                {Nol = "000";}
                else if(BAN.length()==2)
                {Nol = "00";}
                else if(BAN.length()==3)
                {Nol = "0";}
                else if(BAN.length()==4)
                {Nol = "";}

               TextFieldKode_Kelompok2.setText("BAN" + Nol + BAN);
            } else {
               TextFieldKode_Kelompok2.setText("BAN0001");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
    
