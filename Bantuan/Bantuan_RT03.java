    public final void LoadTable() {
        DefaultTableModel model = new DefaultTableModel();
        TabelBantuanRT03_Kelompok2.setModel(model);
        model.addColumn("Kode");
        model.addColumn("Tanggal");
        model.addColumn("No KK");
        model.addColumn("NIK");
        model.addColumn("Nama");
        model.addColumn("Bantuan");
        model.addColumn("Keterangan");
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.configDB();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM bantuan_rt03";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("Kode");
                o[1] = r.getDate("Tanggal");
                o[2] = r.getString("NoKK");
                o[3] = r.getString("NIK");
                o[4] = r.getString("Nama");
                o[5] = r.getString("Bantuan");
                o[6] = r.getString("Keterangan");
               
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terdapat Kesalahan, Harap Periksa Lagi Program/Kodingannya");
        }
    }
    
