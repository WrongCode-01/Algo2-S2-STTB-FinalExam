    private void FindData(String key){
        try{
            DefaultTableModel model = new DefaultTableModel();
            Object[] judul_kolom = {"Kode","Tanggal","NoKK", "NIK", "Nama", "Bantuan", "Keterangan"};
            model=new DefaultTableModel(null,judul_kolom);
            TabelBantuanRT03_Kelompok2.setModel(model);
            
            Connection conn=(Connection)Koneksi.configDB();
            Statement stt=conn.createStatement();
            model.getDataVector().removeAllElements();
            
            String sql = ("SELECT * from bantuan_rt03 WHERE Kode LIKE '%"+key+"%' OR Tanggal LIKE '%"+key+"%' OR NoKK LIKE '%"+key+"%' OR NIK LIKE '%"+key+"%' OR Nama LIKE '%"+key+"%' OR Bantuan LIKE '%"+key+"%' OR Keterangan LIKE '%"+key+"%'");  
            ResultSet rs = stt.executeQuery(sql);
            while(rs.next()){
                Object[] data={
                    rs.getString("Kode"),
                    rs.getDate("Tanggal"),
                    rs.getString("NoKK"),
                    rs.getString("NIK"),
                    rs.getString("Nama"),
                    rs.getString("Bantuan"),
                    rs.getString("Keterangan"),
                };
               model.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
    }
    
